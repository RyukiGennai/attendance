<?php
// 1. 【準備】まずは「ログイン中のメモ（セッション）」にアクセスします。
// これを書かないと、サーバーは「どのメモを消せばいいか」が分かりません。
session_start();

// 2. 【後片付け】サーバーに保存されていたログイン情報を、跡形もなく消去します。
// これが「ロッカーの中身を空にする」という作業です。
// これをやらないと、次に同じパソコンを使った人が、あなたの画面を勝手に見られてしまうかもしれません。
session_destroy();

// 3. 【移動】後片付けが終わったら、自動的に「ログイン画面（001_index.php）」へ飛ばします。
// 「はい、これで終わりですよ」と最初の受付に戻してあげるイメージです。
header('Location: 001_index.php');

// 4. 【終了】このプログラムの実行をここで完全に終了させます。
// 移動（リダイレクト）した後は、もう何もする必要がないからです。
exit;